import { cpSync, existsSync, lstatSync, mkdirSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const SCRIPT_DIR = dirname(fileURLToPath(import.meta.url));
const PROJECT_ROOT = resolve(SCRIPT_DIR, "..");
const OUTPUT_DIR = resolve(PROJECT_ROOT, process.env.SCAFFOLD_EXPORT_DIR || "dist/scaffold");

const EXPORT_ENTRIES = Object.freeze([
  ".env.example",
  ".gitignore",
  "eslint.config.js",
  "remotion.config.js",
  "vite.config.js",
  "preview/index.html",
  "preview/main.jsx",
  "preview/App.jsx",
  "preview/public/preview-favicon.svg",
  "preview/scaffold",
  "remotion/index.jsx",
  "remotion/ProjectRoot.jsx",
  "scripts/dev-workbench.mjs",
  "scripts/remotion-render.mjs",
  "shared/scaffold",
  "shared/project",
  "shared/styles/index.css",
  "shared/features/demoMotion",
]);

const EXPORTED_SCRIPT_KEYS = Object.freeze([
  "preview:dev",
  "remotion:studio",
  "remotion:bundle",
  "remotion:compositions",
  "remotion:render",
  "remotion:render:demo",
  "dev",
  "lint",
  "verify",
]);

const copyEntry = (entryPath) => {
  const sourcePath = resolve(PROJECT_ROOT, entryPath);
  if (!existsSync(sourcePath)) {
    throw new Error(`[scaffold:export] missing required path: ${entryPath}`);
  }

  const targetPath = resolve(OUTPUT_DIR, entryPath);
  mkdirSync(dirname(targetPath), { recursive: true });

  const stats = lstatSync(sourcePath);
  if (stats.isDirectory()) {
    cpSync(sourcePath, targetPath, { recursive: true });
    return;
  }

  cpSync(sourcePath, targetPath);
};

const buildScaffoldPackageJson = () => {
  const packageJsonPath = resolve(PROJECT_ROOT, "package.json");
  const raw = readFileSync(packageJsonPath, "utf8");
  const rootPackage = JSON.parse(raw);
  const baseName = rootPackage.name || "remotion-scaffold";
  const scaffoldName = baseName.endsWith("-scaffold")
    ? baseName
    : `${baseName}-scaffold`;
  const exportedScripts = Object.fromEntries(
    EXPORTED_SCRIPT_KEYS.filter((key) => typeof rootPackage.scripts?.[key] === "string").map((key) => [
      key,
      rootPackage.scripts[key],
    ])
  );

  return {
    name: scaffoldName,
    private: true,
    version: "0.1.0",
    type: rootPackage.type || "module",
    scripts: exportedScripts,
    dependencies: rootPackage.dependencies || {},
    devDependencies: rootPackage.devDependencies || {},
  };
};

const writeScaffoldReadme = () => {
  const readmePath = resolve(OUTPUT_DIR, "README.md");
  const content = `# Scaffold Export

This directory was generated by:

\`\`\`bash
npm run scaffold:export
\`\`\`

## Quick Start

\`\`\`bash
cd ${process.env.SCAFFOLD_EXPORT_DIR || "dist/scaffold"}
npm install
npm run dev
\`\`\`

## Replace For New Project

- Replace \`shared/features/demoMotion\` with your own feature plugin.
- Update \`shared/project/projectRegistry.js\` to register your plugin/compositions.
- Update \`shared/project/projectConfig.js\` to choose the active composition.
`;
  writeFileSync(readmePath, content, "utf8");
};

const main = () => {
  rmSync(OUTPUT_DIR, { recursive: true, force: true });
  mkdirSync(OUTPUT_DIR, { recursive: true });

  for (const entryPath of EXPORT_ENTRIES) {
    copyEntry(entryPath);
  }

  const scaffoldPackageJson = buildScaffoldPackageJson();
  writeFileSync(
    resolve(OUTPUT_DIR, "package.json"),
    `${JSON.stringify(scaffoldPackageJson, null, 2)}\n`,
    "utf8"
  );
  writeScaffoldReadme();

  console.log(`[scaffold:export] exported scaffold to ${OUTPUT_DIR}`);
};

main();
